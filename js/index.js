function fetchGithub(keyword, by) {
    let url = by==="name"
    ? `https://api.github.com/search/users?q=${keyword}` 
    : `https://api.github.com/users/${keyword}/repos`

    fetch(url)
    .then(response => response.json())
    .then(data => {
        if(by==="name") {
            createCards(data)
        } else {
            createRepolist(data)
        }
    })
}

